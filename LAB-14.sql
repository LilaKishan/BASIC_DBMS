ALTER FUNCTION SUMOFEVEN1TO20()
RETURNS INT 
AS
BEGIN
DECLARE @S INT =1,@ANS INT =0
WHILE @S<=20
	BEGIN
		IF @S%2=0
			BEGIN
				 SET @ANS=@ANS+@S
			END
			SET @S=@S+1
	END
	RETURN @ANS
END

SELECT DBO.SUMOFEVEN1TO20()

