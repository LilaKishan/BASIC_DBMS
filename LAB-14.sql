ALTER FUNCTION PRINTODD()
RETURNS VARCHAR(100)
AS
BEGIN 
DECLARE @ODD INT,@DEMO VARCHAR(100) =''
SET @ODD=1
WHILE @ODD<=10
	BEGIN
		IF @ODD%2<>0
		BEGIN
		
			 SELECT @DEMO = @DEMO+CAST(@ODD AS VARCHAR(100))+','
		END
		SET @ODD=@ODD+1
	END
	RETURN @DEMO
END
SELECT DBO.PRINTODD()
