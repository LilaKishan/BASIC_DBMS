ALTER FUNCTION PRINT1()
RETURNS VARCHAR(100)
AS
BEGIN 
DECLARE @I INT,@STRING VARCHAR(100) = ''
SET @I=1
WHILE @I<=10
	BEGIN
		 SELECT @STRING = @STRING+CAST(@I AS VARCHAR(100))+','
		SET @I=@I+1
	END
	RETURN @STRING
END

SELECT DBO.PRINT1()

--2--
ALTER FUNCTION ODDEVEN(@J INT)
RETURNS VARCHAR(100)
AS
BEGIN
DECLARE @RETURN VARCHAR(100)=''
	IF @J%2<>0
		BEGIN
			SET @RETURN='ODD'
		END
	ELSE 
		BEGIN 
			SET @RETURN='EVEN'
		END
		RETURN @RETURN
END
SELECT DBO.ODDEVEN(5)

