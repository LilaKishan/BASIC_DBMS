ALTER FUNCTION SUMOF1TO10()
RETURNS INT
AS
BEGIN
DECLARE @S INT =1,@ANS INT =0
WHILE @S<=50
	BEGIN
		 SET @ANS=@ANS+@S
		SET @S=@S+1
	END
	RETURN @ANS
END

SELECT DBO.SUMOF1TO10()

