ALTER FUNCTION PRIMEORNOT(@S INT)
RETURNS VARCHAR(100)
AS
BEGIN
DECLARE @PRIME VARCHAR(100) ='',@I INT =2,@FLAG INT =0
	WHILE @I<@S/2
		BEGIN
			IF @S%@I=0
			BEGIN
				SET @FLAG =1
				BREAK
			END
			ELSE 
			BEGIN
				SET @PRIME='PRIME'
			END
			SELECT @S=@S+1
		END 
		RETURN @PRIME
END

SELECT DBO.PRIMEORNOT(17)
